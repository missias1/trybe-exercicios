//Exercicio 1 - Crie um quadrado de asteriscos. Onde qualquer que seja o valor de n>1, seja impresso um número de linhas e colunas igual a n

let asterisco= "*";
let vazio= " ";
let n=3
for(let i=0; i<n; i+=1){
  asterisco+="*"
  vazio+=" "
  console.log(vazio + asterisco)
}



